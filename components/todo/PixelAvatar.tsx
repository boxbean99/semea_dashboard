export default function PixelAvatar({ memberId, scale = 1 }: { memberId: string; scale?: number }) {
  const char = CHARACTERS[memberId];
  if (!char) return null;

  const size = PX * scale;
  const svgW = COLS * size;
  const svgH = ROWS * size;

  return (
    <svg
      width={svgW}
      height={svgH}
      viewBox={`0 0 ${svgW} ${svgH}`}
      style={{ imageRendering: "pixelated", display: "block" }}
    >
      {char.grid.flatMap((row, y) =>
        row.map((key, x) => {
          if (key === "_") return null;
          const color = char.palette[key];
          if (!color) return null;
          return (
            <rect key={`${x}-${y}`} x={x * size} y={y * size} width={size} height={size} fill={color} />
          );
        })
      )}
      {char.overlay && (
        <text
          x={svgW / 2}
          y={svgH * 0.42}
          textAnchor="middle"
          dominantBaseline="middle"
          fill="#c8d6f0"
          fontSize={size * 4}
          fontWeight="900"
          fontFamily="monospace"
          opacity={0.85}
        >
          {char.overlay}
        </text>
      )}
    </svg>
  );
}
